    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/JavaProject","root","Lord@sanomee7");
            Statement st = con.createStatement();
            String sql= "select * from Recruiter";
            ResultSet rs = st.executeQuery(sql);
            DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
            while (rs.next())
            {
                String Name = rs.getString("RNAME");
                String CName = rs.getString("COMPANYNAME");
                String Job = rs.getString("RJOB");
                String MobileNo = String.valueOf(rs.getInt("RMOBILENO"));
                String Email = String.valueOf(rs.getString("REMAIL"));
                
                String[] tbData={Name,CName,Job,MobileNo,Email};
                if(Job.equals(jTextField1.getText()))
                {
                    
                    tblModel.addRow(tbData);
                }
                
                
                
                
            }
          
            con.close();
        }    
        catch(Exception e)
        {
             e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

